function fillSelect(selectId, data, valueField, textField) {
    const select = document.getElementById(selectId);
    const currentValue = select.value;
    
   
    const firstOption = select.options[0];
    select.innerHTML = '';
    select.appendChild(firstOption);
    
   
    data.forEach(item => {
        const option = document.createElement('option');
        option.value = item[valueField];
        option.textContent = item[textField];
        select.appendChild(option);
    });
    
  
    select.value = currentValue;
}


async function loadClientes() {
    try {
        const response = await fetchAuth(`${API_URL}/clientes`);
        
        if (!response.ok) {
            throw new Error('Error al cargar clientes');
        }
        
        clientes = await response.json();
        renderClientes(clientes);
        
    } catch (error) {
        console.error('Error:', error);
        showNotification('Error al cargar los clientes', 'error');
    }
}


function renderClientes(clientesList) {
    const tbody = document.getElementById('clientesTableBody');
    
    if (clientesList.length === 0) {
        tbody.innerHTML = `
            <tr>
                <td colspan="8" style="text-align: center; padding: 40px;">
                    No hay clientes registrados
                </td>
            </tr>
        `;
        return;
    }
    
    tbody.innerHTML = clientesList.map(cliente => `
        <tr>
            <td><strong>${cliente.idCliente}</strong></td>
            <td>${cliente.nombres}</td>
            <td>${cliente.apellidos}</td>
            <td>${cliente.numDoc || '-'}</td>
            <td>${cliente.telefono || '-'}</td>
            <td>${cliente.correo}</td>
            <td>${cliente.nroLicencia || '-'}</td>
            <td class="table-actions">
                <button class="btn btn-sm btn-secondary" onclick="editCliente('${cliente.idCliente}')">
                    Editar
                </button>
                <button class="btn btn-sm btn-danger" onclick="deleteCliente('${cliente.idCliente}')">
                    Eliminar
                </button>
            </td>
        </tr>
    `).join('');
}


document.getElementById('searchInput').addEventListener('input', (e) => {
    const searchTerm = e.target.value.toLowerCase();
    
    const filtered = clientes.filter(cliente => 
        cliente.nombres.toLowerCase().includes(searchTerm) ||
        cliente.apellidos.toLowerCase().includes(searchTerm) ||
        (cliente.numDoc && cliente.numDoc.includes(searchTerm)) ||
        (cliente.correo && cliente.correo.toLowerCase().includes(searchTerm))
    );
    
    renderClientes(filtered);
});




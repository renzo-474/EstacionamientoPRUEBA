function editCliente(idCliente) {
    const cliente = clientes.find(c => c.idCliente === idCliente);
    if (!cliente) return;
    
    isEditMode = true;
    currentClienteId = idCliente;
    
    document.getElementById('modalTitle').textContent = 'Editar Cliente';
    document.getElementById('nombres').value = cliente.nombres;
    document.getElementById('apellidos').value = cliente.apellidos;
    document.getElementById('numDoc').value = cliente.numDoc || '';
    document.getElementById('nroLicencia').value = cliente.nroLicencia || '';
    document.getElementById('telefono').value = cliente.telefono || '';
    document.getElementById('correo').value = cliente.correo;
    document.getElementById('codSexo').value = cliente.codSexo || '';
    document.getElementById('codNacio').value = cliente.codNacio || '';
    document.getElementById('codDistrito').value = cliente.codDistrito || '';
    document.getElementById('direccion').value = cliente.direccion || '';
    
  
    const distritoWrapper = document.querySelector('.searchable-select');
    if (distritoWrapper && cliente.codDistrito) {
        const distrito = distritos.find(d => d.codDistrito === cliente.codDistrito);
        if (distrito) {
            distritoWrapper.querySelector('.search-input').value = distrito.nombre;
        }
    }
    
    document.getElementById('clienteModal').classList.add('show');
}

// Eliminar cliente
async function deleteCliente(idCliente) {
    if (!confirm(`¿Está seguro de eliminar el cliente ${idCliente}?`)) {
        return;
    }
    
    try {
        const response = await fetchAuth(`${API_URL}/clientes/${idCliente}`, {
            method: 'DELETE'
        });
        
        if (response.ok) {
            showNotification('Cliente eliminado exitosamente', 'success');
            loadClientes();
        } else {
            throw new Error('Error al eliminar cliente');
        }
        
    } catch (error) {
        console.error('Error:', error);
        showNotification('Error al eliminar el cliente', 'error');
    }
}

